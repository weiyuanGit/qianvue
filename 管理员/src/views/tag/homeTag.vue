<template>
	<div>
		<el-row class="title-box">
			信息信息表付个
		</el-row>
		<el-row class="content-box">
			<el-row>
				<el-col :span="8">
					<el-form label-width="80px">
						<el-form-item label="状态">
							<el-select v-model="searchData.status" placeholder="请选择状态">
								<el-option v-for="(item,index) in generalStatus" :key="index" :label="item.name" :value="item.value"></el-option>
							</el-select>
						</el-form-item>
					</el-form>
				</el-col>
			</el-row>
			<el-row>
				<el-button type="primary" @click="searchBtn">查询</el-button>
				<el-button type="primary" @click="getContentsBtn">默认列表</el-button>
			</el-row>

		</el-row>

		<el-popover placement="right" width="400" trigger="click">
			<el-col :span="20">
				<el-input placeholder="请输入标签名称" v-model="tagTitle" style="display: inline-block;width: 400px"></el-input>
				<el-button @click="subBtn">提交</el-button>
			</el-col>
			<el-button slot="reference">添加首页标签</el-button>
		</el-popover>
		<el-row class="table-box">
			<el-table :data="tableData" border style="width: 100%">
				<el-table-column prop="name" label="名称" width="400">
				</el-table-column>
				<el-table-column prop="status" label="状态" :formatter="tagStatusFliter">
				</el-table-column>
				<el-table-column label="操作" width="100">
					<template slot-scope="scope">
						<el-button @click="infoBtn(scope.row)" type="text" size="small">查看详情</el-button>
						<el-button type="text" size="small">编辑</el-button>
						<el-button @click="closeBtn(scope.row)" type="text" size="small">禁用</el-button>
					</template>
				</el-table-column>
			</el-table>
		</el-row>

		<el-row style="height: 80px;">
			<el-col :span="24">
				当前页数：{{page}}
				<el-button type="primary" size="small" :disabled="page==1" @click="changePage(page-1)">上一页</el-button>
				<el-button type="primary" size="small" :disabled="pageOver" @click="changePage(page+1)">下一页</el-button>
			</el-col>
		</el-row>
	</div>
</template>

<script>
	export default {
		name: "homeTag",
		data() {
			return {
				tagTitle: '',
				tableData: [],
				count: 10,
				page: 1,
				pageOver: true,
				searchData: {
					type: '',
					status: '',
					power: '',
					tags: '',

				},

				typeList: this.$constData.typeList,
				tagstatus: this.$constData.tagstatus,
				powerList: this.$constData.powerList,
				generalStatus: this.$constData.generalStatus,
			}
		},
		methods: {
			/** 过滤器*/
			typeFliter(row, col, val) {
				let typeList = this.typeList
				for (let i = 0; i < typeList.length; i++) {
					if (typeList[i].value == val) {
						return typeList[i].name
					}
				}
			},
			timeFliter(row, col, val) {
				let timer = new Date(val)
				let dataTime = timer.toLocaleDateString() + ' ' + timer.toLocaleTimeString('chinese', {
					hour12: false
				})
				return dataTime
			},
			tagStatusFliter(row, col, val) {
				let tagstatus = this.tagstatus
				for (let i = 0; i < tagstatus.length; i++) {
					if (tagstatus[i].value == val) {
						return tagstatus[i].name
					}
				}
			},
			powerFliter(row, col, val) {
				let powerList = this.powerList
				for (let i = 0; i < powerList.length; i++) {
					if (powerList[i].value == val) {
						return powerList[i].name
					}
				}
			},



			/*获取内容列表*/
			getContents(cnt) {
				console.log(cnt)
				//this.$util.RC.SUCCESS=> 'succ'
				//this.$util.tryParseJson => json.parse()
				cnt.status = 1,
					this.$api.getContentTag(cnt, (res) => {
						console.log(res)
						if (res.data.rc == this.$util.RC.SUCCESS) {
							this.tableData = this.$util.tryParseJson(res.data.c)
						} else {
							this.tableData = []
						}
						if (this.tableData.length < this.count) {
							this.pageOver = true
						} else {
							this.pageOver = false
						}
						console.log(this.$util.tryParseJson(res.data.c))
					})
			},

			/* 分页*/
			changePage(page) {
				this.page = page
				//获取内容列表
				let cnt = {
					moduleId: this.$constData.module,
					count: this.count,
					offset: (this.page - 1) * this.count
				}
				this.getContents(cnt)
			},
			/* 查询数据*/
			searchBtn() {
				console.log(this.searchData)
				this.page = 1
				let cnt = {
					moduleId: this.$constData.module,
					count: this.count,
					offset: (this.page - 1) * this.count
				}


				if (this.searchData.status) {
					cnt.status = this.searchData.status
				}

				this.getContents(cnt)
			},



			/* 禁用标签*/
			closeBtn(info) {
				this.$confirm('是否继续?', '提示', {
					confirmButtonText: '确定',
					cancelButtonText: '取消',
					type: 'warning'
				}).then(async () => {
					let cnt = {
						moduleId: this.$constData.module,
						group: '首页',
						name: info.name, 
						status: 0,
					}
					this.$api.editteContentTag(cnt, (res) => {
						if (res.data.rc == this.$util.RC.SUCCESS) {
							this.$message({
								type: 'success',
								message: '成功!'
							});
						} else {
							this.$message({
								type: 'error',
								message: '操作失败!'
							});
						}
					})
				}).catch(() => {
					this.$message({
						type: 'info',
						message: '已取消'
					});
				});
			},
			/* 添加首页标签*/
			subBtn() {
				let cnt = {
					moduleId: this.$constData.module, // Long 模板编号
					group: '首页', // String 分组编号
					name: this.tagTitle,
				}
				this.$api.createContentTag(cnt, (res) => {
					if (res.data.rc == this.$util.RC.SUCCESS) {
						this.$message({
							type: 'success',
							message: '成功!'
						});
					} else {
						this.$message({
							type: 'error',
							message: '操作失败!'
						});
					}
				})
			},


			//查看 详情
			infoBtn(info) {
				this.$router.push({
					path: '/svipInfoList',
					name: 'svipInfoList',
					params: {
						info: info
					}
				})
			},

			//获取默认列表
			getContentsBtn() {
				this.searchData.type = ''
				this.searchData.status = ''
				this.searchData.power = ''
				this.searchData.tags = ''
				this.page = 1
				let cnt = {
					moduleId: this.$constData.module,
					group: '首页',
					count: this.count,
					offset: (this.page - 1) * this.count,
				}
				//this.getContents(cnt)
			}


		},
		mounted() {
			//获取内容列表
			let cnt = {
				moduleId: this.$constData.module,
				group: '首页',
				count: this.count,
				offset: (this.page - 1) * this.count,
			}
			this.getContents(cnt)


		}
	}
</script>

